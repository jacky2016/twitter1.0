package com.xunku.app.model;

public class ImageStruct {
	int width;
	int height;
	int[] image;
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int[] getImage() {
		return image;
	}
	public void setImage(int[] image) {
		this.image = image;
	}
}
