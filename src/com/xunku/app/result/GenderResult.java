package com.xunku.app.result;

/**
 * 性别分布
 * 
 * @author wujian
 * @created on Jul 21, 2014 5:58:57 PM
 */
public class GenderResult {

	double females;// 女
	double males;// 男
	double others;// 其它
	
	public double getFemales() {
		return females;
	}
	public void setFemales(double females) {
		this.females = females;
	}
	public double getMales() {
		return males;
	}
	public void setMales(double males) {
		this.males = males;
	}
	public double getOthers() {
		return others;
	}
	public void setOthers(double others) {
		this.others = others;
	}
}
